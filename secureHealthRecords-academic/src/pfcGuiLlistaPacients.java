    public void estableixDades (String idMetge, String contrasenyaMetge, String pathP12Metge, GestorInterRemot gestor, pfcHistorialGuiMetge pfcHistorialGuiMetge ) {
        
        this.ncolMetge = idMetge;
        this.contrasenyaMetge = contrasenyaMetge;
        this.pathP12Metge = pathP12Metge;
        this.metge = new Metge ( this.ncolMetge, this.contrasenyaMetge, this.pathP12Metge );
        this.gestor = gestor;        
        this.pfcHistorialGuiMetge = pfcHistorialGuiMetge;
        
        estableixLlistatPacientsModel();
        
    }    
    
    
    public void estableixLlistatPacientsModel () {        
     
        
         try {
             
             org.jdom.Document a = metge.procedure1();
             org.jdom.Document b = gestor.procedure2(a);
             org.jdom.Document c = metge.pas3( b , "Llista_pacients");             
             boolean autentificacio = gestor.pas4 ( c );                        

             if (autentificacio) {
                                
                     org.jdom.Document llistatPacients = gestor.procedure5(this.ncolMetge);

                     java.util.Vector<String> vectorLlistatPacients;
                     vectorLlistatPacients = metge.procedure6(llistatPacients);
                    
                     for(int i=0;i<vectorLlistatPacients.size();i++) {                         
                         //llistaPacientsModel.addElement( (String)vectorLlistatPacients.get(i));
			llistaPacientsModel.addElement( vectorLlistatPacients.get(i));                                                  
                     } 
                     
             }                                         
                     
                                                                         
            }   catch (Exception e)     {

                               e.printStackTrace();
            }
    }
    
   
    
